void alternateMergeLinkedList(LinkedList *ll1, LinkedList *ll2)
{
    ListNode *temp1;
    ListNode *temp2;
    ListNode *cur1,*cur2;

    if(ll1->size == 0 || ll2->size == 0 || ll1->head == NULL || ll2->head == NULL){
        return;
    }

    else if(ll1->head != NULL && ll2->head != NULL){
        temp1 = ll1->head;          /*Assign temp pointer that points to first node of LL1*/
        temp2 = ll2->head;          /*Assign temp pointer that points to first node of LL2*/
    }

    while(ll2->head != NULL && ll2->size != 0){

        cur2 = temp2->next;         /*Keep track of the 2nd node in LL2 using a pointer, placed before the if block so cur2 is activated first*/
        
        if(temp1->next == NULL){
            temp2->next = NULL;
            temp1->next = temp2;

            ll2->size = ll2->size - 1;
            ll1->size = ll1->size + 1;

            ll2->head = cur2;
            break;
        }

        cur1 = temp1->next;         /*Keep track of the 2nd node in LL1 using a pointer*/
        
        temp2->next = temp1->next;  /*Disconnect 1st node of LL2 from 2nd node of LL2 and join to 2nd node of LL1*/
        temp1->next = temp2;        /*Disconnect 1st node of LL1 from 2nd node of LL1 and join to first node of LL2*/

        ll2->head = cur2;           /*Update head pointer of LL2 using the 2nd node pointer from before*/
        ll2->size = ll2->size - 1;  /*Decrease LL2 size by 1 because one node has gone to LL1*/
        temp2 = cur2;               /*Move the temp pointer to the new first node of LL2 which is the same address as the 2nd node of LL2*/

        ll1->size = ll1->size + 1;  /*Increase LL1 size by 1 because one node from LL2 has come to LL1*/
        temp1 = cur1;               /*Move the temp pointer to the 2nd node of LL1 which is also the 3rd node of the newly merged list*/       
    }

   return;
}

